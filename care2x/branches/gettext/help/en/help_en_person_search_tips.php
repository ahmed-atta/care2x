</ul>
</blockquote>

<p><strong>Searching for all adam's smith's born in year 1947.</strong></p>

<blockquote>
<ul class="noindent">
<li>Type the first name , the family name and the year of birth<br />
</li>
</ul>
</blockquote>

<p><strong>Searching for all persons born in year 1989.</strong></p>

<blockquote>
<ul class="noindent">
<li>Type the *, type space, type *, type space, type the year of birth<br />
</li>
</ul>
</blockquote>

<p><strong>Searching for all adam's smith's born in 20th century (1900 to 1999).</strong></p>

<blockquote>
<ul class="noindent">
<li>Type "adam", type space, type "smith", type space, type "19".<br />
</li>
</ul>
</blockquote>
