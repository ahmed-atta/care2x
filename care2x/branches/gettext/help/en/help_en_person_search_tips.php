<strong>Tips &amp; tricks in searching a person</strong> 

<p><strong>Important to remember!</strong></p>

<blockquote>Any word that you enter will be considered as a probable beginning of the information.

<p>So, if for example you entered "Adam", Care2x will search for all family names (or first names) that begin with "adam". A search result will probably contain names like "Adam", "Adamson", "Adamo", etc.</p>
</blockquote>

<p><strong>What is the fastest way to search for a person?</strong></p>

<blockquote>
<ul class="noindent">
<li>Type his full personal ID number (PID).</li>
</ul>
</blockquote>

<p><strong>How to search by family name?</strong></p>

<blockquote>
<ul class="noindent">
<li>Just enter the name or few beginning characters in the input.</li>

<li>Click the "Search" button.<br />
</li>
</ul>
</blockquote>

<p><strong>Why include the first name in searching?</strong></p>

<blockquote>
<ul class="noindent">
<li>If the "Search for first names too" radio button is shown, you need to click it (activate) to force Care2x to search by both family and first names. Otherwise (if button is un-clicked), only the family names will be searched.<br />
</li>
</ul>
</blockquote>

<p><strong>Why should I limit my search by family names alone?</strong></p>

<blockquote>
<ul class="noindent">
<li>The search will be faster.</li>
</ul>
</blockquote>

<p><strong>FAST search by first name alone!</strong></p>

<blockquote>
<ul class="noindent">
<li>Type the first name, type space, then type <strong>*</strong>.<br />
</li>
</ul>
</blockquote>

<p><strong>Searching by first and family name.</strong></p>

<blockquote>
<ul class="noindent">
<li>Type the first and family name separating them with a space<br />
<p>Note: The abovemost notice applies. The first and second words will be considered as probable beginning of first and family names respectively. So, the above entry example would probably return a list that also contains the "Adamson Smithsonian", "Adams Smiths", "Adamhoe Smitheren", etc.</p>
</li>
</ul>
</blockquote>

<p><strong>Searching by family and first name.</strong></p>

<blockquote>
<ul class="noindent">
<li>If you want to type the family name first followed by the first name:</li>

<li>Type the family name followed immediately by a comma, then a space, and finally the first name.<br />
</li>
</ul>
</blockquote>

<p><strong>Searching for all persons born in November 1, 1967 (01.10.1967).</strong></p>

<blockquote>
<ul class="noindent">
<li>Type the complete date of birth following your current date format.<br />
</li>
</ul>
</blockquote>

<p><strong>What is my current date format?</strong></p>

<blockquote>
<ul class="noindent">
