  <strong>How to find the patient's data?</strong> 

<div style="margin-left: 2em"><strong>Step 1:</strong> Enter the patient's patient or case number in the " <span style="background-color:yellow">Patient number</span> " field.<br />
 <strong>Step 2:</strong> Click the button <input type="button" value="Search" /> to find the patient.<br />
 <strong>Step 3:</strong> When the patient is found, his basic data will appear in the corresponding fields.<br />
 <strong>Step 4:</strong> If all or most of the photos are taken on the same date, enter the date in the "&nbsp;<span style="background-color:yellow">Shotdate</span>&nbsp;" field.<br />
 <strong>Step 5:</strong> Click the button to set this date for all the photos. This date will automatically appear in the "Shotdate" fields on the left frame.

<p><strong>Note!</strong> If one or some photos must have a different date, enter the unique date into the photo's corresponding "Shotdate" field. You can only do this after you are finished with step 5.</p>
</div>

