<p>  <strong>The entry fields are displayed. How to select the image files?</strong></p>

<div style="margin-left: 2em"><strong>Step 1:</strong> Click the button <input type="button" value="Browse..." /> to find the file of the photo you want to store.<br />
 <strong>Step 2:</strong> A "Select file" window will pop up. Select the file you want and click "Open".<br />
 <strong>Step 3:</strong> If the file you selected is a valid graphic file format, a preview of the photo will appear on the upper right frame (MSIE browser only). Otherwise, repeat step 1 and 2.<br />
 <strong>Step 4:</strong> Enter the date when this photo was taken in the " <span style="background-color:yellow">Shotdate</span> " field.

<p><strong>Step 5:</strong> Enter the photo number in the " <span style="background-color:yellow">Number</span> " field.</p>

