<strong>How to change the number of images for uploading?</strong> 

<div style="margin-left: 2em"><strong>Step 1:</strong> Enter the number in the "I want to upload <input type="text" name="v" size="4" maxlength="4" /> image(s) " field.

<p><strong>Step 2:</strong> Click "Go"</p>
