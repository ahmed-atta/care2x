<strong>How to search a medocs document</strong> 

<p>  <strong>Step 1</strong></p>

<div style="margin-left: 2em">Enter in the "<span style="background-color:yellow">Medocs document of:</span>" field either a full information or a few letters from a patient's information, like for example patient's number, or name, or Given name.

<p>Example 1: enter "21000012" or "12".<br />
Example 2: enter "Guerero" or "gue".<br />
Example 3: enter "Alfredo" or "Alf".</p>
</div>

<strong>Step 2</strong> 

<div style="margin-left: 2em">Click the button to start the search.</div>

<strong>Step 3</strong> 

<div style="margin-left: 2em">If the search finds a single result the complete medocs document will be displayed. If however, the search finds several results, a list will be displayed.

