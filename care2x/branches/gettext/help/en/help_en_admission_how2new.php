<strong>How to admit a patient</strong> 

<p><strong>Step 1</strong></p>

<div style="margin-left: 2em">Verify first if the patient's basic data already exists.

<p>Enter either a full information or a few letters from a patient's information, like for example first name, or family name, or the PID (person identifier).</p>

<p>Example 1: enter "21000012" or "12".<br />
 Example 2: enter "Guerero" or "gue".<br />
 Example 3: enter "Alfredo" or "Alf".</p>
</div>

<strong>Step 2</strong>
<div style="margin-left: 2em">Click the button to start searching. Your search will be directed to the person search module.</div>

<strong>Step 3</strong>
<div style="margin-left: 2em">If the search does not find any result, you need to register the person first. Follow the instructions for registering the person.</div>

<strong>Step 4</strong>
<div style="margin-left: 2em">If the search finds a result, select the right person from the displayed list by clicking the button beside it.</div>

<strong>Step 5</strong>
<div style="margin-left: 2em">Once the admission form is displayed, enter all relevant admission data.</div>

<strong>Step 6</strong>
<div style="margin-left: 2em">Click the button to save the admission information.

<p></p>
</div>

<strong>Note</strong>
<div style="margin-left: 2em">If an information is missing, the entries will be redisplayed and a message will appear prompting you to enter the information in the field or fields marked red. Afterwards, click the button to save the complete information.</div>

<strong>Note</strong>
<div style="margin-left: 2em">If you want to save an incomplete information click the button <input type="button" value="Save anyway" />.</div>

