<p><strong>How to discharge a patient?</strong></p>

<div style="margin-left: 2em"><strong>Step 1:</strong> Make sure the correct discharge date and time are entered.<br />
<strong>Step 2:</strong> Select the discharge type by clicking its corresponding radio button<br />
<p><strong>Step 3:</strong> Type additional notice or notes about the discharge in the " <span style="background-color:yellow">Notice:</span> " field if available.<br />
 <strong>Step 4:</strong> Type your name in the " <span style="background-color:yellow">Nurse: <input type="text" name="a" size="20" maxlength="20" /></span>" field if it is empty.<br />
 <strong>Step 5:</strong> Check the " <span style="background-color:yellow"><input type="checkbox" name="d" value="d" /> Yes, I'm sure. discharge the patient.</span> " field.<br />
 <strong>Step 6:</strong> Click the button <input type="button" value="discharge" /> to discharge the patient.</p>

<p><strong>Step 7:</strong> Click the button align="absmiddle"&gt; to go back to the ward's new occupancy list.</p>
</div>

