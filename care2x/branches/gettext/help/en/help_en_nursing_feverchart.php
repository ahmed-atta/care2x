<a id="cbp" name="cbp"></a> How to enter a temperature or blood pressure? 

<div style="margin-left: 2em"><strong>Step 1:</strong> Click on the chart area corresponding to the chosen date.<br />
 <strong>Step 2:</strong> A pop-up window for entering the temperature and/or blood pressure data will appear.<br />
 <strong>Step 3:</strong> Enter the data and the time.<br />
<ul type="disc">
<li>Enter the time and temperature on the right "Temperature" column.<br />
 Example: <input type="text" name="t" size="5" maxlength="5" value="12.35" />&nbsp;&nbsp;<input type="text" name="u" size="8" maxlength="8" value="37.3" /></li>

<li>Enter the time and blood pressure on the left "Blood pressure" column.<br />
 Example: <input type="text" name="v" size="5" maxlength="5" value="10.05" />&nbsp;&nbsp;<input type="text" name="w" size="8" maxlength="8" value="128/85" /></li>
</ul>

<div style="margin-left: 2em"><strong>Tip:</strong> To enter the current time, type "n" or "N" (meaning NOW) in the time field. The exact current time will appear automatically in that field.</div>

<strong>Step 4:</strong> If you have several data, enter all of them before you save.<br />
 <strong>Step 5:</strong> Click the button to save the newly entered data.<br />
 <strong>Step 6:</strong> If you want to correct any errors, click on the erroneous data and replace it with the correct one.<br />
 <strong>Step 5:</strong> If you are finished, click the button to close the window and go back to the chart.<br />
 <a href="#howto">Back to "How to...?"</a></div>

<a id="movedate" name="movedate"></a> How to move or change the chart's date? 

<ul>
<li><strong>To move the date one day backward:</strong><br />
 <strong>Step 1:</strong> Click on the icon on the <span style="background-color:yellow">leftmost</span> date column of the chart.

<p></p>
</li>

<li><strong>To move the date one day forward:</strong><br />
 <strong>Step 1:</strong> Click on the icon on the <span style="background-color:yellow">rightmost</span> date column of the chart. 

<p></p>
</li>

<li><strong>To directly set the chart's starting date:</strong><br />
 <strong>Step 1:</strong> Click the <span style="background-color:yellow">right mouse button</span> on the icon on the <span style="background-color:yellow">leftmost</span> date column of the chart.<br />
 <strong>Step 2:</strong> Click <input type="button" value="OK" /> when asked for confirmation.<br />
 <strong>Step 3:</strong> A small pop-up window will appear showing the selection fields for the starting date.<br />
 <strong>Step 4:</strong> Select the day, month, and year.<br />
 <strong>Step 5:</strong> Click the button <input type="button" value="GO" /> to enable the change.<br />
 The small window will automatically close and the chart will adjust to the date's change.

<p></p>
</li>

