 <strong>I want to edit a station's profile other that is currently displayed. What should I do?</strong>
<p><strong>Note:</strong> You cannot simply edit a station's profile. This is by design to avoid detroying data integrity. To have a new station profile please follow the steps below:</p>

<div style="margin-left: 2em"><strong>Step 1:</strong>Make sure that the ward is empty of patients, either discharged or transferred to other wards or departments.

<p><strong>Step 2:</strong> Click on the <input type="button" value="Irreversibly close this ward" /> button to close the ward.</p>

<p><strong>Step 3:</strong> Create a new ward using the "Create" option of the ward management module.</p>

<p></p>
</div>

<strong>What's the purpose of "closing the ward temporarily" ?</strong>
<p></p>

<div style="margin-left: 2em"><strong>Answer:</strong> Closing the ward temporarily is done when the ward will not accept patients for a certain period of time. For example when the ward is due to renovation works, repair, desinfection, decontamination, etc.</div>

