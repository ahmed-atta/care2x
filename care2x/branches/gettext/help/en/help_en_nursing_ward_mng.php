  <strong>How to create a new ward?</strong> 

<div style="margin-left: 2em"><strong>Step 1:</strong> Enter the ward's formal name in the " <span style="background-color:yellow">Ward:</span> " field.<br />
 <strong>Step 1:</strong> Enter the ward ID (keep it shor with no special characters) in the " <span style="background-color:yellow">Ward ID:</span> " field.<br />
 <strong>Step 2:</strong> Select the department or clinic to where the ward belongs in the " <span style="background-color:yellow">Department:</span> " selection field.<br />
 <strong>Step 3:</strong> Write the ward's description and other information in the " <span style="background-color:yellow">Description:</span> " field.<br />
 <strong>Step 4:</strong> Enter the first room's number in the ward in the " <span style="background-color:yellow">Room number of the first room:</span> " field.<br />
 <strong>Step 5:</strong> Enter the last room's number in the ward in the " <span style="background-color:yellow">Room number of the last room:</span> " field.<br />
 <strong>Step 6:</strong> Enter the prefix for the room in the " <span style="background-color:yellow">Room prefix:</span> " field.<br />
 <strong>Step 7:</strong> Click the <input type="button" value="Create the ward" /> button to create the ward.<br />
</div>

<strong>Note</strong> 

<div style="margin-left: 2em">If you decide to cancel click the button .</div>

