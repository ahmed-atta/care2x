<strong>Ward's profile data</strong>
<div style="margin-left: 2em">This option will show the ward's profile and other relevant data.</div>

<strong>Lock a bed</strong>
<div style="margin-left: 2em">If you want to lock a bed or several beds at once, click this option. The logged ward will be displayed or if not available the default ward will displayed. Locking a bed requires a valid password with access right for this function.</div>

<strong>Access rights</strong>
<div style="margin-left: 2em">In this option you can create, edit, lock, or delete access rights for a particular ward. All the access rights created will have an access only in that particular ward.</div>

   <strong>How to show the ward's occupancy list?</strong>
<div style="margin-left: 2em"><strong>Step 1:</strong> Click the ward's id or name on the left column.<br />
 <strong>Note:</strong> The occupancy list that will be displayed is "read only". You cannot edit or change any patients' data.<br />
</div>

<strong>How to show the ward's occupancy list for editing or updating data?</strong>
<div style="margin-left: 2em"><strong>Step 1:</strong> Click the icon corresponding to the chosen ward.<br />
 <strong>Step 2:</strong> If you have logged in before and you have access right for the function, the occupancy list will be displayed immediately.<br />
 Otherwise, you will be asked to enter your username and password.<br />
 <strong>Step 3:</strong> If asked, enter your username and password.<br />
 <strong>Step 4:</strong> Click the button <input type="button" value="Continue..." />.<br />
 <strong>Step 5:</strong> If you have an access right for the function, the occupancy list will be displayed.<br />
 <strong>Note:</strong> The occupancy list that will be displayed can be "edited". Options for editing or updataing patients' data will be displayed. You can also open the patients' data folder for further editing.<br />
</div>

