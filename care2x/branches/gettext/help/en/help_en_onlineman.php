<strong>How to use the online manual</strong> 

<p>The online manual is internet based. You can open it only if your system has an internet access or if your firewall allows you to access the internet.</p>

<p><strong>How to open the online manual</strong></p>

<blockquote>Click the "Online manual" item on the left menu.</blockquote>

<p><strong>Finding help quickly</strong></p>

<blockquote>
<ul class="noindent">
<li>Use the "Find Page" function of the online manual. Click the "FindPage" link on the lower left of the page.

<p></p>
</li>

<li>Enter a title of the page in the first entry field, then click the "Search" button.</li>

<li>Or you can enter a keyword derived from your needed help (for example: "daily diet") in the second entry field, then click the "Search" button.

<p></p>
</li>
</ul>
</blockquote>
