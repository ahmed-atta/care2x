<a id="show_panel" name="show_panel"></a> What is "Show control panel" option on the upper left of the image? 

<blockquote>This option will activate the java control buttons for manipulating the image.

<p><strong>Control functions:</strong></p>

<ul class="noindent">
<li>Reset: Resets the image to the its original</li>

<li>Flip: Flips the image 180&deg; vertically. (Vertical mirroring)</li>

<li>Invert: Inverts the color table of the image</li>

<li>Lighten: Brightens the image</li>

<li>Darken: Darkens the image</li>

<li>Rotate: Rotates the image 30 degrees clockwise (right)</li>

<li>Zoom in</li>

<li>Zoom out</li>

<li>Threshold: Adjusts the color to average</li>

<li>Smooth: Smoothens the image</li>

<li>Sharpen: Sharpens the image</li>

<li>Find edges</li>
</ul>
</blockquote>

<a id="edit_imagej" name="edit_imagej"></a> What is "Edit image with ImageJ" option on the top part of the image? 

<blockquote>This option will activate the external java applet ImageJ, a powerful and feature rich image processor.

<p>Activating this option will launch the ImageJ and put the currently selected image in a pop-up window for processing.</p>
</blockquote>

