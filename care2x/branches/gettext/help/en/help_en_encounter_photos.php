What is "Show control panel" option on the upper left of the image?</b></font>
<blockquote>
	This option will activate the java  control buttons  for manipulating the image.<p>
	<b>Control functions: </b>
	<li><font color="blue">Reset</font>: Resets the image to the its original</li>
	<li><font color="blue">Flip</font>: Flips the image 180° vertically. (Vertical mirroring)</li>
	<li><font color="blue">Invert</font>: Inverts the color table of the image</li>
	<li><font color="blue">Lighten</font>: Brightens the image</li>
	<li><font color="blue">Darken</font>: Darkens the image</li>
	<li><font color="blue">Rotate</font>: Rotates the image 30 degrees clockwise (right)</li>
	<li><font color="blue">Zoom in</font></li>
	<li><font color="blue">Zoom out</font></li>
	<li><font color="blue">Threshold</font>: Adjusts the color to average</li>
	<li><font color="blue">Smooth</font>: Smoothens the image</li>
	<li><font color="blue">Sharpen</font>: Sharpens the image</li>
	<li><font color="blue">Find edges</font></li>
</blockquote>

