<blockquote>
<p>"<strong>News display</strong> " formats the look and feel of the news and headline articles:</p>

<p>What you can configure to format the display of news articles?</p>

<p></p>

<ul class="noindent">
<li>Title font size</li>

<li>Title font type</li>

<li>Title font color : click the icon to launch the color picker.</li>

<li>Title font weigth : <strong>bold</strong> or normal</li>

<li>Lead summary font size</li>

<li>Lead summary font type</li>

<li>Lead summary font color : click the icon to launch the color picker.</li>

<li>Lead summary font weigth : <strong>bold</strong> or normal</li>

<li>News body font size</li>

<li>News body font type</li>

<li>News body font color : click the icon to launch the color picker.</li>

<li>Lead summary (news preview) maximum number of displayed characters.</li>

<li>News display width: either in pixel or percentage. Note: for percentage, append the % character after the value (e.g. 100% for the entire page width)</li>

<li style="list-style: none">
<p>NOTE: If you enter an invalid value, the system will just replace it with the default value.</p>

<p>To use the default values, click the <button>Use default values</button> button.</p>

