<b>System Admin :: Paginator max rows</b></font>
<p>
<font size=2 face="verdana,arial" >
<ul>
<p>
	<b>Paginator max rows</b> is the maximum number or rows listed per page when a search routine returns a valid data.
	<p>When the actual number or rows returned are higher than the configured maximum rows, the data will be displayed in several pages (paginated). Each
	displayed page will only list up to the configured maximum number of rows.</p>
	<p><li>Note 1: Each type of search can be configured to its own maximum number of rows.</p>
	<li>Note 2: If the value is set to zero, the system will use an internal default value of 50.</li>
	<p>
	<li>Note 3: The maximum for "Patient for OR operation search" should be set to a value
	lower than 10 (preferably only 5) due to a narrow display space in the OR logbook module.</li>
</ul>
</font>

