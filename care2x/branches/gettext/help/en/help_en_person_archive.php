<strong>How to search for information using the archive</strong> 

<p><strong>Step 1</strong></p>

<div style="margin-left: 2em">Enter either a full information or a few letters from a person's information, like for example first name, or family name, birthdate, or the PID (person identifier).

<p>Example 1: enter "21000012" or "12".<br />
 Example 2: enter "Guerero" or "gue".<br />
 Example 3: enter "Alfredo" or "Alf".<br />
 Example 4: enter "10.1" or "10.12.1956".</p>

<p>Note: You can combine several information to narrow your search.<br />
 For example: Select sex and civil status.<br />
</p>
</div>

<strong>Step 2</strong>
<div style="margin-left: 2em">Click the button to start the search.</div>

<strong>Step 3</strong>
<div style="margin-left: 2em">If the search finds a result or results, a list will be displayed.<br />
 To see the registration data of the person you are looking for, click the button beside it.</div>

<strong>Note</strong>
<div style="margin-left: 2em">If you decide to cancel search click the button .</div>
