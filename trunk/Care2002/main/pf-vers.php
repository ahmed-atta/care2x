 Nun gibt es neuen Wirbel um das staatliche Vorsorgeinstrument. Grund: Bei der Pflegeversicherung sind &Uuml;bersch&uuml;sse in H&ouml;he von zw&ouml;lf Milliarden Mark aufgelaufen. Und das, obwohl durch die Einnahmen lediglich laufende Ausgaben gedeckt werden sollten. Der Aufbau eigener Kapitalreserve ist dagegen, ebenso wie bei Kranken-, Arbeitslosen- und Rentenversicherung, im System nicht vorgesehen war. <P>

