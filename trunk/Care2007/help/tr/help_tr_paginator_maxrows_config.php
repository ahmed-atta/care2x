	<b>Sayfalayýcý azami satýr:</b> Bir arama sonucunda geçerli veriler bulunduðunda bir sayfada listelenen azami satýr sayýsýdýr.
	<p>Bulunan satýr sayýsý sayfalayýcýnýn azami satýr sayýsýndan fazla ise bilgiler birkaç sayfa halinde görüntülenir. Görüntülenen her sayfa en fazla azami satýr sayýsý kadar satýr içerir.</p>
	<p><li>Uyarý 1: Her arama türü için özel azami satýr nuumarasý belirlenebilir.</p>
	<li>Uyarý 2: Eðer deðer 0 olarak ayarlanýr ise sistem ön tanýmlý olarak 50 deðerini kullanýr.</li>
	<p>
	<li>Uyarý 3: "Ameliyathane ameliyat aramasý için hasta" sayýsý azami deðeri 10 dan küçük ayarlanmalýdýr. Ameliyathane kütük modülündeki görüntüleme yeri darlýðýndan dolayý tercihan sadece 5 olmalýdýr. </li>
