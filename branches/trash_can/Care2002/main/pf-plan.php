<img src="../img/pplanu.jpg" border="0" align="right">

<FONT face="arial,helvetica" color="black" size=-1>
Der Krankenpflegeprozess besteht aus einigen logisch aufgebauten und voneinander abh&auml;ngigen &Uuml;berlegungs-,
Entscheidungs- und Handlungs- schritten. Alle Schritte sollten auf ein Ziel ausgerichtet sein, welches man auch als Probleml&ouml;sung bezeichnen kann.<P>
Sehr wichtig ist ein geschlossenes Bild zu erhalten, der sogennante R&uuml;ckkopplungseffekt, durch den die Pflege 
beurteilt und wenn notwendig neu angepa&szlig;t werden.<P>

Das Resultat der Pflege wird am Pflegeziel gemessen.



<P>
Wenn das Ziel erreicht ist, ist der Vorgang beendet. Wenn aber Abweichungen vom gesetzten Ziel vorkommen, oder neue
Probleme auftreten, beginnt der ganze Proze&szlig; von neuem. Es m&uuml;ssen dann zus&auml;tzliche Informationen 
gesammelt werden, Probleme und Ziele neu formuliert und die Ma&szlig;nahmen entsprechend angepa&szlig;t werden.<P>

Pflege ist ein zwischenmenschlicher Beziehungsproze&szlig;, bei dem zwei Personen (Patient und Pflegeperson) Kontakt
zueinander aufnehmen um ein gemeinsames Ziel zu erreichen, das PFLEGEZIEL. Die Qualit&auml;t dieser Beziehung spielt
eine wesentliche Rolle im Probleml&ouml;sungsprozess.<BR>
